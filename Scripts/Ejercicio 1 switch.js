let a = Number( prompt("Digite un numero"));

switch (a) {
  case 1:
    alert( 'Es Lunes' );
      break;
  case 2:
    alert( 'Es Martes' );
      break;
  case 3:
    alert( 'Es Miercoles' );
      break;
  case 4:
    alert( 'Es Jueves' );
      break;
  case 5:
    alert( 'Es Viernes' );
      break;
  case 6:
    alert( 'Es Sabado' );
      break;
  case 7:
    alert( 'Es Domingo' );
      break;
  default:
    alert( 'No existe ese dia de la semana' );
}